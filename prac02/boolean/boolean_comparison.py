# Boolean comparison
# Операторы сравнения возвращают True или False

a = 10
b = 5

print(a == b)   # False (равно)
print(a != b)   # True  (не равно)
print(a > b)    # True
print(a < b)    # False
print(a >= b)   # True
print(a <= b)   # False

# Сравнение строк
name1 = "Python"
name2 = "python"

print(name1 == name2)   # False (регистр важен)

# Сравнение чисел и результатов выражений
x = 7
print(x * 2 == 14)      # True
